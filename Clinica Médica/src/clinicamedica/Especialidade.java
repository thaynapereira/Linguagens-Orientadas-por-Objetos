package clinicamedica;

public class Especialidade {

	public String nome;
	public int vagas;
	
	public Especialidade (String nome, int vagas)
	{
		this.nome=nome;
		this.vagas=vagas;
		
	}
	public String getNome() {
		return this.nome;
	}
	
	public int getVagas() {
		return this.vagas;
	}
	
	
}
	
	

